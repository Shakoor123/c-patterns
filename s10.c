#include <stdio.h>
#include <stdlib.h>

int main(void) {
	int n=5,i,j,k;
	for(i=1;i<=n;i++){
		for(j=1;j<=i;j++){
			for(k=1;k<=i;k++){
				printf("*");
			}
			printf("\n");
		}
		printf("\n");
	}
}
